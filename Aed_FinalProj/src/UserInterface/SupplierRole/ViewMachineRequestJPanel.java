    public void populateTable() {

        boolean flag;

        DefaultTableModel model = (DefaultTableModel) tblMachineRequests.getModel();

        model.setRowCount(0);
        flag = false;
        for (WorkRequest work : system.getWorkQueue().getWorkRequestList()) {
            if (work instanceof MachineRequest) {
                if (work.getStatus().equals("Pending") || work.getStatus().equals("Completed")) {
                    Object[] row = new Object[8];
                    row[0] = work;
                    row[1] = work.getMachine();
                    row[2] = work.getMachine().getCode();
                    row[3] = ((MachineRequest) work).getRequestQuantity();
                    row[4] = work.getSender();
                    row[5] = work.getReceiver();
                    row[6] = work.getStatus();
                    row[7] = work.getDayOfDelivery();
                    model.addRow(row);
                    flag = true;
                }
            }
            if (flag == false) {
                JOptionPane.showMessageDialog(null, "Please Add Machines");

            }

        }
    }


