    private void btnAssigntoMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssigntoMeActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        int selectedRow = tblMachineRequests.getSelectedRow();

        if (selectedRow >= 0) {
            MachineRequest req = (MachineRequest) tblMachineRequests.getValueAt(selectedRow, 0);

            if (req.getStatus().equalsIgnoreCase("Completed")) {
                JOptionPane.showMessageDialog(null, "Request already processed");
                return;
            }
            for (Machine m : s.getMachineList().getMachineCatalog()) {
                if (req.getMachine().equals(m)) {
                    flag = true;
                    Date oDate = estimatedDate.getDate();
                    Date now = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("MM-dd-yyyy");
                    if ((sdf.format(oDate).equals(sdf.format(now))) || (sdf.format(oDate).compareTo(sdf.format(now))) > 0) {
                        DateFormat oDateFormat = new SimpleDateFormat("MM-dd-yyyy");
                        String szDate = oDateFormat.format(oDate);
                        req.setReceiver(this.ua);
                        req.setStatus("Completed");

                        req.setDayOfDelivery(szDate);

                        estimatedDate.setDate(oDate);
                        populateTable();
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid Date of Delivery");
                        return;
                    }
                }
            }
            if (flag == false) {
                JOptionPane.showMessageDialog(null, "You cannot request as you dont have the machine!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Request to process");
            return;
        }

    }//GEN-LAST:event_btnAssigntoMeActionPerformed

