    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNotifyTechnician.getSelectedRow();
        if (selectedRow >= 0) {
            String des = txtProblemDesc.getText();
            String type = "";
            if (jRBMaintenance.isSelected()) {
                type = "Maintenance";
            }
            if (jRBguidelines.isSelected()) {
                type = "Guidelines";
            }
            if (type.equals("")) {
                JOptionPane.showMessageDialog(null, "Please input type of service");
                return;
            } else if (des.equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter Description");
                return;
            } else {
                techReq = new TechnicianRequest();
                techReq.setMachine(machineReq.getMachine());
                techReq.setSender(ua);
                org.getWorkQueue().getWorkRequestList().add(techReq);
                ua.getWorkQueue().getWorkRequestList().add(techReq);
                system.getWorkQueue().getWorkRequestList().add(techReq);
                techReq.setDescription(des);
                techReq.setServiceType(type);
                techReq.setStatus("Requested");
                techReq.setSender(ua);
                populateTechnicianRequest();
                JOptionPane.showMessageDialog(null, "Request send successfully");
                buttonGroup1.clearSelection();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select the request!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSendActionPerformed

