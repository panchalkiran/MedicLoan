    public void populateMachineRequest() {
        DefaultTableModel dtm = (DefaultTableModel) tblNotifyTechnician.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[7];
        row[0] = machineReq.getMachine().getName();
        row[1] = machineReq.getMachine().getCode();
        dtm.addRow(row);
    }

    public void populateTechnicianRequest() {
        DefaultTableModel dtm = (DefaultTableModel) tblNotifyTechnician.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[7];
        row[0] = techReq.getMachine().getName();
        row[1] = techReq.getMachine().getCode();
        row[2] = ((TechnicianRequest) techReq).getServiceType();
        row[3] = techReq.getStatus();
        row[4] = techReq.getReceiver();
        row[5] = ((TechnicianRequest) techReq).getServiceDateInString();

        dtm.addRow(row);
    }

