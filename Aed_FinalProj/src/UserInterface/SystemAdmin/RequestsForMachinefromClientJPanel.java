    private void btnSendSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendSupplierActionPerformed
        
// TODO add your handling code here:
        int selectedRow = tblReqfromClients.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MachineRequest req = (MachineRequest) tblReqfromClients.getValueAt(selectedRow, 0);

            if (req.getStatus().equalsIgnoreCase("Requested")) {
                for (Network net : network) {
                    for (Enterprise e : net.getEnterpriseDirectory().getEnterpriseList()) {
                        for (Organization o : e.getOrganizationDirectory().getOrganizationList()) {
                            if (o instanceof SupplierOrganization) {
                                o.getWorkQueue().getWorkRequestList().add(req);
                                req.setStatus("Pending");
                                populateTable();

                            }
                        }
                    }
                }
                //JOptionPane.showMessageDialog(null, "Sent to Supplier");
            } else if (req.getStatus().equalsIgnoreCase("Pending")) {
                JOptionPane.showMessageDialog(null, "Supplier Notified. Waiting for Reply!!");
            } else if (req.getStatus().equalsIgnoreCase("Completed")) {
                JOptionPane.showMessageDialog(null, "Order Completed!!");
            }
        }

    }//GEN-LAST:event_btnSendSupplierActionPerformed

    public void populateLabels() {
        int selectedrow = tblReqfromClients.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            machineDetails.show();
            MachineRequest machine = (MachineRequest) tblReqfromClients.getValueAt(selectedrow, 0);
            lblMachineName.setText(machine.getMachine().getName());
            lblCode.setText(machine.getMachine().getCode());

            lblQty.setText(String.valueOf(machine.getRequestQuantity()));
            lblPrise.setText(String.valueOf(machine.getTotalPrice()));
            lblQty.setText(String.valueOf(machine.getRequestQuantity()));

            double totalPrice = machine.getMachine().getPrice() * machine.getMachine().getQuantity();
            DecimalFormat df = new DecimalFormat("#.00");

            double totalAmount = machine.getTotalPrice();
            lblPrise.setText(df.format(totalAmount));
            lblSender.setText(machine.getSender().getUsername());
            lblStatus.setText(machine.getStatus());

            if (machine.getStatus().equalsIgnoreCase("Requested")) {
                lblReceiver.setText("");
                lblDateOfDelivery.setText("");

            } else if (machine.getStatus().equalsIgnoreCase("Pending")) {
                lblReceiver.setText("Notified");
                lblDateOfDelivery.setText("-");

            } else if (machine.getStatus().equalsIgnoreCase("Completed")) {
                lblReceiver.setText(machine.getReceiver().getEmployee().getName());
                lblDateOfDelivery.setText(machine.getDayOfDelivery());
                btnGenerateBill.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Please select Machine from Table ");
            }
        }

    }
    
