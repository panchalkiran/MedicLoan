    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblReqfromClients.getModel();
        dtm.setRowCount(0);

        for (WorkRequest r : system.getWorkQueue().getWorkRequestList()) {
            if (r instanceof MachineRequest) {
                Object[] row = new Object[10];
                row[0] = r;
                row[1] = r.getMachine();
                row[2] = r.getMachine().getCode();
                row[3] = ((MachineRequest) r).getRequestQuantity();
                double totalAmount = ((MachineRequest) r).getTotalPrice();
                DecimalFormat df = new DecimalFormat("#.00");
                row[4] = df.format(totalAmount);
                row[5] = r.getSender().getRole();
                row[6] = r.getSender();
                row[7] = r.getStatus();
                row[8] = r.getReceiver() == null ? null : r.getReceiver().getEmployee().getName();
                row[9] = r.getDayOfDelivery();

                dtm.addRow(row);
            }
            txtMailAdd.setText(r.getSender().getEmployee().getEmailId());
        }
    }

